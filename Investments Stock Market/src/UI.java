    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (init == 0){
        ArrayList<AgentController> agents = new ArrayList<>();
        // get a JADE runtime
        Runtime rt = Runtime.instance();
        // create a default profile
        Profile p1 = new ProfileImpl();
        Profile p2 = new ProfileImpl();
        Profile p3 = new ProfileImpl();
        // create the Main-container
        ContainerController mainContainer1 = rt.createAgentContainer(p1);
        ContainerController mainContainer2 = rt.createAgentContainer(p2);
        ContainerController mainContainer3 = rt.createAgentContainer(p3);


        // create all agents but Analyzer
        try {

            String[] argss = {};
            AgentController ac1 =
                    mainContainer1.createNewAgent("procura", "SearchAgent",argss);
            agents.add(ac1);

            AgentController ac2 =
                    mainContainer2.createNewAgent("sector", "SectorAgent",argss);
            agents.add(ac2);

            AgentController ac3 =
                    mainContainer3.createNewAgent("analisador", "AnalyzerAgentUI",argss);
            agents.add(ac3);

            for(int i=0;i<agents.size();i++){
                agents.get(i).start();
            }
            init = 1;
        } catch (jade.wrapper.StaleProxyException e) {
            System.err.println("Error launching agent...");
        }
        }else  JOptionPane.showMessageDialog(this,"All agents already initialized!");

    }//GEN-LAST:event_jButton3ActionPerformed

